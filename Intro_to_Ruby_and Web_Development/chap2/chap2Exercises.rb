# How many hours are in a year?
puts 24 * 365

# How many minutes are in a decade?
# 2 extra days for the two leap years in a decade
puts 60 * 24 * ((365 * 10) + 2)

# Your age in seconds. How many seconds old are you?
puts 60 * 60 * 24 * 365 * 35

# If I am 1,111 million seconds old (which I am, though I was somewhere 
# in the 800 millions when I started this book), how old am I?
puts 1111000000 / 60 / 60 / 24 / 365